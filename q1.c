#include<stdio.h>

int main(){
	
	int num,i;
	int factorial = 1 ;
	
	printf("Enter the number you want to factorial \n");
	scanf("%d",&num);
	
	
	for (i = 1 ; i <= num ; i++){
		factorial = factorial * i
		;}
	printf("Factorial of your num is %d" ,factorial);
	
	return 0;	
	
		


}

